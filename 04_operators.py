# Arithmetic Operators

a = 10
b = 5
c = a + b  # Addition
print(c)


# Assignment Operators

a = 4-2 # Assign 4-2 in a 
print(a)

b += 3 # Increment the value of b by 3 and then assign it to b
b -= 1 # Decrement the value of b by 1 and then assign it to b
print(b)

# Comparison Operators

d = 5>2 # Greater than
e = 3<7 # Less than
f = 4==4 # Equal to
g = 5!=3 # Not equal to
h = 6>=6 # Greater than or equal to
i = 2<=3 # Less than or equal to    
print(d, e, f, g, h, i)

# Logical Operators

e = True or False # Logical OR
# Truth table of 'OR'
print("true or false is", True or False)
print("true or true is", True or True)
print("false or true is", False or True)
print("false or false is", False or False)


# Truth table of 'AND'
print("true and false is", True and False)
print("true and true is", True and True)
print("false and true is", False and True)
print("false and false is", False and False)

print(not(True or False)) # Logical NOT
print(not(False and True)) # Logical NOT    
