# Write a python program to calculate the square of a number entered by the user.
a = int(input("Enter a number to find its square: "))
print("The square of the number is: ", a ** 2)
print("The square of the number is: ", a * a)
print("The square of the number is: ", a^2) # This will raise an error because '^' is not the exponentiation operator in Python